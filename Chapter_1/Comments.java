public class Comments 
{
    public static void main(String[] args) 
    {

        // Single-line comment: simple greeting
        System.out.println("Single-line comment syntax: begins with two forward slashes (//).");

        /* Multi-line comment: explaining a block of code
         * 
         * 
         * 
         */
        System.out.println("Multi-line comment syntax: begins with /* and ends with */.");

        /**
         * Documentation comment: describes the purpose of this print statement
         * 
         * 
         */
        System.out.println("Documentation comment syntax: begins with /** and ends with */.");       
    }
}
